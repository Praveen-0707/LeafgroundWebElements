package roughworks;

public class PrintNumbersWithoutUsingNumbersInCode {

	public static void main(String[] args) {

//		WAP to print 1 - 100 without using numbers in code
		
		int num = 'A'/'A';
		String str = "**********";
		int cnt = str.length();
		
		for (int i=num; i<cnt*cnt; i++)
		{
			System.out.println(i);
		}

	}

}
